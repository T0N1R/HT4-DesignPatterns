class Operaciones {
    
    public int Suma(int num1, int num2){
	int sumres = num1 + num2;
        
        return sumres;
    }


    public int Resta(int num1, int num2){
	int resres = num1 - num2;
        
        return resres;
    }


    public int Multipicacion(int num1, int num2){
	int mulres = num1 * num2;
        
        return mulres;
    }


    public int Division(int num1, int num2){
	int devres = num1 / num2;
        
        return devres;
    }
    
}
